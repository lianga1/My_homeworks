# 字典是由{键：值}成一组形成的可变容器
# dict()函数,或直接{key : value}
a = {123 : 'hello' , 234 : 'world'}
b = dict(((102,'张三') , (112,'李四') , (123,'王五')))
# 上例是将三个元组先组成一个元组，再按每组分为键和值
c = dict([(102,'张三') , (112,'李四') , (123,'王五')])
# 上例是将三个元组组成一个列表，然后再分为键和值
d = dict(zip([12,23,34],['张三', '李四','王五']))
print(a)
print(b)
print(c)
print(d)