# 文件位置：dev_env/py/数据运算/位运算.py
a = 16
b = 2
b1 = ~a
print(b1)# 位反运算，将a的数值按位取反，公式：~a == (a + 1) * (-1)
b2 = a & b
print(b2)# 位与运算，按位取与运算，如00000111与00111110结果为00000110
b3 = a | b
print(b3)# 位或运算，按位进行或运算，如上两个数结果为00111111
b4 = a ^ b
print(b4)# 为异或运算，按位进行异或运算，如上两个数结果为00111001
b5 = a >> b
print(b5)# 右移运算，将a右移b位，高位采用符号位补位（正为0，负为1）
b6 = a << b 
print(b6)# 左移运算，将a左移b位，低位用0补位